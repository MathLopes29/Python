"""
Variáveis e Tipos de Dados

str : String
int: Inteiro
float: Flutuantes
j: Números Complexos
boolean: V ou F

"""

nome : str;
nome = "Carlos D'Angelo";
print(type(nome));
print(nome[7:15]);

valor : int;
valor = 18;
print(type(valor));

valor_Flutuante : float;
valor_Flutuante = 18.9;
print(type(valor_Flutuante));

valor_Complexo = 1j
print(type(valor_Complexo))

var_Bool = True;
print(type(var_Bool))

